// pergunta 1
document.getElementById('check1').addEventListener('click', function() {
    var answer = document.getElementById('answer1').value.trim().toLowerCase();
    var resposta = document.getElementById('resposta1');
    if (answer === 'd') {
        resposta.textContent = 'Resposta correta!';
        resposta.style.color = 'green';
    } else {
        resposta.textContent = 'Resposta incorreta. Tente novamente.';
        resposta.style.color = 'red';
    }
});


